def calculate_result(scores: dict) -> dict:
    """
    Принимает словарь {"A": int, "B": int, "C": int, "D": int}
    и возвращает словарь с описанием результата (RESULTS[...]).
    Если несколько типов набрали максимум баллов — возвращается «Защитник».
    """
    max_score = max(scores.values())
    leaders = [t for t, v in scores.items() if v == max_score]
    if len(leaders) > 1:
        return RESULTS["TIE"]
    return RESULTS[leaders[0]]


def format_result_text(result: dict) -> str:
    lines = [f"{result['emoji']} {result['name']}"]
    if result.get("subtitle"):
        lines.append(result["subtitle"])
    lines.append("")
    lines.append(result["desc"])
    lines.append("")
    lines.append(f"Чемпионы: {result['champions']}")
    return "\n".join(lines)
